SELECT 
    "Клиент",
    "Тип",
    ROUND("Текущая задолженность", 2) AS "Задолженность, руб.",
    ROUND("Оплачено в 2024", 2) AS "Оплачено, руб.",
    ROUND("Всего внесено в 2024", 2) AS "Внесено, руб.",
    ROUND("Сальдо", 2) AS "Сальдо, руб.",
    "Финансовый статус",
    CASE 
        WHEN "Текущая задолженность" > 5000 THEN 'Высокий риск'
        WHEN "Текущая задолженность" > 1000 THEN 'Средний риск'
        WHEN "Текущая задолженность" > 0 THEN 'Низкий риск'
        WHEN "Сальдо" < -1000 THEN 'Значительная переплата'
        WHEN "Сальдо" < 0 THEN 'Незначительная переплата'
        ELSE 'Стабильный'
    END AS "Категория риска",
    
    CASE 
        WHEN "Текущая задолженность" > 5000 THEN 'Срочный контакт, возможна блокировка'
        WHEN "Текущая задолженность" > 1000 THEN 'Направление уведомления, телефонный контакт'
        WHEN "Текущая задолженность" > 0 THEN 'Автоматическое напоминание'
        WHEN "Сальдо" < -1000 THEN 'Предложить зачёт переплаты'
        ELSE 'Стандартное обслуживание'
    END AS "Рекомендуемые действия"
    
FROM mv_client_financial_status
WHERE "Текущая задолженность" > 0 
    OR "Сальдо" <> 0
    OR "Финансовый статус" != 'Баланс'
ORDER BY 
    CASE "Финансовый статус"
        WHEN 'Есть задолженность' THEN 1
        WHEN 'Переплата' THEN 2
        ELSE 3
    END,
    "Текущая задолженность" DESC,
    "Клиент";